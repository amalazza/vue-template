    <p>&ldquo;Kelihatannya, ada kabar gembira nih dari Bapak.&rdquo; Aku bersalaman dengan tangan keriput milik Bapak. Senyuman Bapak belum juga sirna dari bibirnya.</p>
    
    <p>&ldquo;Ini untuk kamu.&rdquo; Bapak tersenyum lagi kearahku. Heran, mengapa sosok satu ini bak malaikat penjaga dimataku?</p>
    
    <p>&ldquo;Bapak dapat uang sebanyak ini darimana?&rdquo; Aku kembali dibuat heran dengan puluhan lembar uang seratus ribuan yang baru saja Bapak berikan kepadaku.</p>
    
    <p>Bapak menggenggam tanganku erat. &ldquo;Pakai saja uangnya untuk biaya sekolahmu, Nak.&rdquo; Ucapnya penuh dengan kesabaran.</p>
    
    <p>&ldquo;Bapak mencuri?&rdquo;</p>
    
    <p>&ldquo;Bapak tidak mungkin menghalalkan segala cara untuk mendapatkan uang sebanyak ini.&rdquo;</p>
    
    <p>Aku yang semakin curiga tanpa henti bertanya pada Bapak yang masih mengukir senyuman tersebut. &ldquo;Bapak pinjam uangnya siapa?&rdquo;</p>
    
    <p>&ldquo;Bapak pinjam uangnya tetangga sebelah. Ada tetaangga yang baik hati meminjamkan uang sebanyak ini kepada Bapak.&rdquo; Bapak menjelaskan hal itu kepadaku, &ldquo;besok segera bayar uang tagihan sekolahnya ya, jangan ditunda-tunda biar kamu nggak dikeluarin dari sekolah.&rdquo; Ujar Bapak kembali seraya memelukku erat.</p>
    
    <p>Aku terisak dipelukan hangatnya, tak tega melihat Bapak berjuaang sekeras ini demi keberlangsungan hidupku. &ldquo;Naya janji akan belajar yang rajin, biar Bapak bisa bangga sama Naya.&rdquo; Ucapku lembut padanya.</p>
    
    <p>Malamnya sebelum aku tertidur pulas, aku menangis tersedu-sedu kala membayangkan sosok Bapak yang susah payah mencari pundi-pundi uang untukku. Biaya hidup semakin mahal, mencari uang semakin tak gampang, dan semesta menuntutnya untuk selalu tegar menghadapi drama kehidupan. Disaat aku ingin berputus asa, Bapak selalu memberiku sebuah semangat lewat senyumannya. Bapak adalah sosok yang sangat berharga untukku, bagaimanapun kondisinya, ia akan tetap sempurna dimataku. Apapun pekerjaannya, beliau tetap menjadi pria terkaya yang ada dikehidupan anaknya. Cintanya yang selalu ada untukku, senyumannya yang selalu menjadi penyemangatku dan sorot matanya yang selalu mampu mebuatku terharu. Begitu sempurnanya sosok Bapak dimataku, ingin aku berterimakasih kepada Tuhan yang sudah mengirimkan sosok setangguh dia dihidupku.</p>
    
    <p>&ldquo;Tuhan, jagalah dia yang begitu tulus mencintaiku.&rdquo;</p>
    
