    <p>&ldquo;Naya..&rdquo; Sebuah suara lembut mengalun perlahan diindera pendengaranku. Sudah kuduga, itu suara Bapak yang sedari tadi kutunggu kehadirannya.</p>
    
    <p>Aku menatap sayu kearahnya, sosok tegar itu berdiri tegap diambang pintu. &ldquo;Bapak tidak kehujanan, kan?&rdquo; Tanyaku khawatir padanya.</p>
    
    <p>&ldquo;Tidak, lihat saja baju Bapak. Tidak basah sama sekali, kamu jangan khawatirkan keadaan bapak.&rdquo; Aku tidak tahu Bapak sedang berbohong atau tidak. Sosok pahlawan satu ini selalu berusaha untuk tertawa diatas beban kehidupan yang ada.</p>
    
    <p>&ldquo;Bapak tidak sedang berbohong, kan?&rdquo;</p>
    
    <p>&ldquo;Naya, sejak kapan bapak pandai berbohong denganmu?&rdquo;</p>
    
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Aku tidak mengerti lagi, mengapa laki-laki yang sangat kucintai ini selalu tegar dalam menerima kenyataan. Aku tidak setegar bapak, aku gadis yang sangat rapuh ketika harus menerima kepergian ibu untuk selama-lamanya. Namun Bapak tidak selemah itu, beliau tetap berusaha tersenyum untuk menguatkanku. Lima tahun sudah aku hidup hanya dengan Bapak, dan saat ini umurku genap diangka 17. Semakin tua usia, semakin banyak pula beban penderitaannya. Hingga detik ini, aku belum mampu untuk membahagiakan bapak. Sedangkan bapak selalu bisa membuatku tertawa dan tersenyum disetiap harinya.</p>
    
    <p>&ldquo;Selamat makan, Naya.&rdquo; Ucap Bapak seraya tersenyum kearahku, senyuman itu benar-benar menenangkan hatiku yang sedang gundah.</p>
    
    <p>&ldquo;Selamat makan juga, Pak.&rdquo;</p>
    
    <p>&ldquo;Kamu terlihat murung hari ini, ada apa?&rdquo; Tanya Bapak kepadaku.</p>
    
    <p>&ldquo;Tidak, Pak.&rdquo; Terpaksa aku harus berbohong kepadanya, aku tidak mau Bapak kepikiran akan suatu hal yang sudah lama kupendam ini.</p>
    
    <p>&ldquo;Bapak tahu kamu sedang berbohong, Naya.&rdquo; Ujar Bapak seraya menyuapkan sesendok nasi bersama lauk tempe goreng tersebut.</p>
    
    <p>Aku hanya mengaduk-aduk nasi yang sudah ada dihadapanku, mengapa dalam sekejap rasa tempe goreng ini hambar dilidahku. &ldquo;Tagihan uang gedung Naya belum dibayar, Pak.&rdquo; Aku masih melihat mata lesu itu berbinar terang bak rembulan, &ldquo;Naya punya rencana, besok Naya akan jualan gorengan. Naya kan dulu sering bantu Almarhumah Ibu jualan, itung-itung buat bantu Bapak nyari uang.&rdquo; Lanjutku dengan nada melemah.</p>
    
    <p>&ldquo;Tidak usah, Nak.&rdquo;</p>
    
    <p>&ldquo;Bapak selalu melarang Naya untuk jualan, padahal Naya kasihan Bapak harus kerja sekeras ini.&rdquo; Aku menyanggah jawabaan singkat Bapak.</p>
    
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Aku tidak tahu lagi, Bapak selalu pura-pura baik-baik saja didepanku. Tubuhnya yang semakin kurus, bahunya yang legam termakan sinar matahari, dan kulitnya yang semakin keriput membuatku tak tega melihatnya terus-menerus bekerja dihari tuanya. Aku benar-benar tidak berguna untuknya, betapa payah dan bodohnya diriku ini. Setiap harinya, aku harus melihat raga ringkih itu berjalan kesana kemari menjemput rezeki. Tak jarang aku melihat Bapak mengusap kasar keringat yang bercucuran dari dahinya. Bodoh, aku bodoh dan sangat bodoh. Aku hanyalah beban untuknya, mengapa Tuhan masih menghidupkanku hingga saat ini?</p>
    
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Malam semakin larut, Bapak sudah masuk ke kamarnya bersiap untuk terlelap. Aku masih sibuk membereskan meja yang tadinya kita pakai untuk makan malam. Beberapa piring dan gelas kotor segera kucuci bersih, selanjutnya ku rapikan taplak meja lusuh tersebut. Tak lama kemudian, aku segera bersiap menuju kamarku untuk bersiap tidur. Tak sengaja ku dapati Bapak yang sudah tertidur pulas diranjang reotnya, diatasnya terdapat topeng badut yang menggantung sempurna disana. Topeng badut itu sudah kotor dan menjadi sarang debu, hanya barang kecil itu yang dapat membantu keluarga kecil ini bertahan hidup.</p>
    
    <p>&ldquo;Tuhan, izinkan aku untuk membahagiakannya.&rdquo; Batinku pelan sambil melihat sosok Bapak yang semakin hari semakin menua itu.</p>
    
